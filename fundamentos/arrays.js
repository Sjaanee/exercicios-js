// Array de quitanda

const horfruit = [
    'Alface',
    'Tomate',
    'Pepino Japônes',
    'maçã',
    122,
    10,
    23,
    45
]

// Push adiciona um dado na estrutura
horfruit.push('Laranja');

console.log(horfruit)

// Pop trás o ultimo elemento do array
console.log(horfruit.pop());

// Delete vai deletar um dado da posição que você passar
delete horfruit[3];
console.log(horfruit)



